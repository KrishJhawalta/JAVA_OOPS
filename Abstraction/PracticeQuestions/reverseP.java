package Abstraction.PracticeQuestions;

public class reverseP {
   public static void main(String[] args) {


       // loop for pattern printing star
       for(int i=1; i<=5; i++){
           for(int j=5; j>=i; j--){
               System.out.print(" * ");
           }
           System.out.println();
       }

    }

}
